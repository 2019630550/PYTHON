#Operadores aritméticos 
a = 10
b = 3
x = 20
y = 7
edad = 25
es_estudiante = True


print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)
print("División entera:", a // b)
print("Módulo:", a % b)
print("Potencia:", a ** b)

#Operadores de comparación
print(x > y)
print(x == y)
print(x != y)

#Operadores lógicos
print(edad > 18 and es_estudiante)
